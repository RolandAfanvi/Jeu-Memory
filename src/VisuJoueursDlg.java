    // methode
    private void afficheTrombi()
    {
        for(int i=0 ; i<this.lj.getNbJoueurs();i++)
        {
            // on recupere le ieme bouton 
            BoutonImage jb =(BoutonImage)Panneau.getComponent(i);
            Joueur j= this.lj.getJoueur(i); //on recupere le joueur d'indice i
            //jb.setIcon(j.getPhoto());
            Image img = j.getPhoto().getImage().getScaledInstance(jb.getWidth(),jb.getHeight(),Image.SCALE_SMOOTH);
            jb.setIcon(new ImageIcon(img));
            
            jb.paint(jb.getGraphics());
        }
    }
    
    
    //Methode pour creer  les boutons pour les images
    private void initTrombi()
    { 
        int nb = this.lj.getNbJoueurs();
        Panneau.setLayout(new GridLayout(1,nb));
        for (int i=0; i< nb; i++)
        {
            BoutonImage jb= new BoutonImage();
            Joueur j= lj.getJoueur(i);
            jb.setName(""+i);
            jb.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
            AfficheInfosJoueur(evt);
            }
            });
            Panneau.add(jb);
        }
        this.pack(); // pour ajuster correctement la taille des composants après les ajouts si besoin
    }
    
