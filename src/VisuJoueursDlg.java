    // methode qui affiche les imformations des joueurs dans la zone d'edition
    private void AfficheInfosJoueur(java.awt.event.ActionEvent evt) { 
    
       for (int i=0 ; i<this.lj.getNbJoueurs(); i++)
       {   BoutonImage jb = (BoutonImage) Panneau.getComponent(i);
           
           if(evt.getSource().equals(jb))
           {
              Edition.setText(this.lj.getJoueur(i).toString());
           }
       }
    }
